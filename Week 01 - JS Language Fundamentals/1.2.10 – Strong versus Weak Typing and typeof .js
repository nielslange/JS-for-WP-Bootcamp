// 1.2.10.1 - Weak / loose typing in  JavaScript

var container = 'Text';
console.log( typeof container );

container = 2;
console.log( typeof container );

// 1.2.10.2 - More weak typing in JavaScript

var container = 'Text';
console.log( typeof container );

container = 2;
console.log( typeof container );

container = true;
console.log( typeof container );

container = { id: 1, title: 'Hello' };
console.log( typeof container );
